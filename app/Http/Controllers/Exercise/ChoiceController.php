    { 
       $exercise =  Exercise::find($id);
       $choice = new Choice();
       $choice->setData($exercise);
       $exercise->ask = $choice->convertAsk();
       $exercise->answer = $choice->convertAnswer();
       $exercise->choice = $choice->convertChoice();
      return  $exercise;
    }

 

